// My current age.
const myAge = 17;

// First two years.
let earlyYears = 2;

earlyYears = earlyYears * 10.5;

// Years in my age after two years.
let laterYears = myAge - 2;

// My "laterYears" age in dog years.
laterYears = laterYears * 4;

//My age in dog years.
const myAgeInDogYears = earlyYears + laterYears;

// My name, lowercased
const myName = "Thony Danielle Labrador".toUpperCase();

//Print
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`)